package com.vinay.synechron.JavaOperators;

public class ArithmeticOperators {

	public static void main(String[] args) {

		int a = 10 ;
		int b = 20 ;


		System.out.println("Arithmetic operator: ");

		System.out.println("num1 + num2: " + (a + b) );
		System.out.println("num1 - num2: " + (a - b) );
		System.out.println("num1 * num2: " + (a * b) ); 
		System.out.println("num1 / num2: " + (a / b) );
		System.out.println("num1 % num2: " + (a % b) );

	}

}
